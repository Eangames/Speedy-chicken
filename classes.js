class Levels{
    constructor(){
        this.levels = [];
        this.startLevel = 0;
        this.level = this.startLevel;
    }
    getNextLevel(scene){
        loadLevel(this.levels[this.level].map, scene);
        this.level++;
    }
    loadLevelNum(level, scene){
        loadLevel(this.levels[level].map, scene);
        this.level = level+1;
    }
}

class Level{
    constructor(levelId, levelName, map){
        this.levelId = levelId;
        this.levelName = levelName;
        this.map = map;
    }
}

